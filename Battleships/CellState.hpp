#ifndef CELLSTATE_HPP
#define CELLSTATE_HPP

/**
 * @enum CellState
 * @brief Перечисление состояний клетки игрового поля
 *
 * Определяет все возможные состояния клетки на поле боя.
 * Используется классом GameBoard для визуализации и логики игры.
 */
enum class CellState {
    Empty,      ///< Пустая клетка (вода) - '~'
    Miss,       ///< Промах - 'O'
    Hit,        ///< Попадание - 'X'
    Ship1,      ///< Однопалубный корабль - '1'
    Ship2,      ///< Двухпалубный корабль - '2'
    Ship3,      ///< Трехпалубный корабль - '3'
    Ship4       ///< Четырехпалубный корабль - '4'
};

#endif